# input values (step 1)

yearBorn = input("What year were you born? ")
yearBorn = int(yearBorn)

thisYear = 2020
thisYear = int(thisYear)

# calculate age (step 2)
yourAge = thisYear - yearBorn
yourAge = int(yourAge)

# Output results
print("You are", yourAge, "year(s) old.")

